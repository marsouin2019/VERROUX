      <h3> <strong>Le Parcours Personnalisé D’intégration Républicaine</strong> </h3>
        <br>
      <h5 class="sousT" > <strong>--> Une offre de service de droit commun mobilisée sur les territoires.</strong> </h5>
        <p>Les préfets de région et de département déclinent au niveau local les orientations stratégiques nationales en matière 
        d’intégration adressées chaque année par le ministre de l’intérieur. Ils assurent la structuration et l’accessibilité de 
        l’offre de services pour mettre en œuvre les parcours d’intégration républicaine adaptés aux besoins des étrangers
         primo-arrivants. Ils veillent à l’articulation des différents acteurs locaux (notamment associatifs) qui accompagnent
          ce public particulier, à la complémentarité des actions et des financements.</p>

        <p>Des actions nombreuses sont soutenues et conduites par les services de l’Etat sur le territoire.
         Elles visent principalement l’apprentissage de la langue française par le biais d’ateliers socio-linguistiques, l’insertion sociale et professionnelle ainsi que l’accès aux droits.
          Des travaux de cartographie ont été initiés pour permettre une meilleure visibilité de l’offre de formation linguistique et d’accès aux droits sur les territoires</p>
