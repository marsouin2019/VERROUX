        <h3> <strong>Le Parcours Personnalisé D’intégration Républicaine</strong> </h3>
        <br>

        <h5 class="sousT" ><strong>-->Parcours personnalisé d’intégration républicaine</strong> </h5>
       
        <p>La loi du 7 mars 2016 relative au droit des étrangers en France réforme le dispositif d’accueil et d’intégration des étrangers accédant pour la première fois au séjour en France et désireux de s’y installer durablement. Elle renforce les outils d’intégration des étrangerslégalement admis en France.</p>
<p> Plusieurs mesures sont entrées en vigueur le 1 er juillet 2016 pour garantir un meilleur accueil et favoriser l’intégration rapide des étrangers primo-arrivants qui s’engagent désormais dans un parcours
personnalisé d’intégration républicaine d’une durée de 5 ans.</p>

<h5 class="sousT"><strong>-->La préparation de l’arrivée en France</strong> </h5>

<p>Un livret d’informations, traduit en plusieurs langues, est accessibledès le pays d’origine sur l’ensemble des sites web du ministère del’intérieur, de l’Office français de l’immigration et de l’intégration
(OFII), des ambassades et des consulats français. Ce livret recense l’ensemble des informations utiles aux étrangers pour préparer leurvenue en France : les valeurs et principes qui caractérisent la société
française et l’ensemble des démarches administratives à accompliravant le départ et lors de l’installation en France.</p>


<h5 class="sousT" ><strong>-->Le contrat d’intégration républicaine,socle d’engagement dans un parcours personnaliséd’intégration républicaine d’une durée de 5 ans</strong> </h5>

<p>La signature d’un contrat d’intégration républicaine (CIR) entré en vigueur le 1 er juillet 2016 marque l’engagement de l’étranger dans leparcours personnalisé d’intégration républicaine. Le CIR se substitue au contrat d’accueil et d’intégration (CAI).</p>

