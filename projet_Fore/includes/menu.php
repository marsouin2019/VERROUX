      <h3> <strong>Le Parcours Personnalisé D’intégration Républicaine</strong> </h3>
        <br>
      <h5 class="sousT" > <strong>--> L’acquisition d’un niveau supérieur de connaissance du français.</strong> </h5>
        <p> La poursuite du parcours de formation linguistique doit permettre à l’étranger d’atteindre le niveau A2 de connaissance du français.
            L’atteinte de ce niveau devient, à compter de mars 2018, une des conditions de délivrance de la carte de résident. 
            Ce niveau permet à la France de se rapprocher des standards européens et de favoriser l’autonomie des étrangers dans notre société, et notamment l’accès à l’emploi et à la formation professionnelle.
            Un second marché de formation linguistique de niveau A2 et B1 a été passé afin d’engager ces formations dès le dernier trimestre 2016.</p>        
        <p>Par ailleurs, une plateforme numérique sera mise à la disposition des étrangers ainsi que des acteurs de l’intégration pour un accès à des formations civique et linguistique, à des informations pratiques, administratives et juridiques, et aux bonnes pratiques dans le domaine.</p>
